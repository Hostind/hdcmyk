/**
 * Get Pantone Color by Name
 * @param {string} colorName - The exact Pantone color name
 * @returns {Object|null} The color object or null if not found
 */
export function getPantoneColor(colorName) {
    return pantoneColors.find(color => color.name === colorName) || null;
}

/**
 * Search Pantone Colors by partial name match
 * @param {string} searchTerm - The search term to match against color names
 * @returns {Array} Array of matching color objects
 */
export function searchPantoneColors(searchTerm) {
    const term = searchTerm.toLowerCase();
    return pantoneColors.filter(color => 
        color.name.toLowerCase().includes(term)
    );
}

/**
 * Find Closest Pantone Color to CMYK Values
 * @param {Object} cmyk - CMYK values {c, m, y, k}
 * @returns {Object} Object with closest color, distance, and similarity percentage
 */
export function findClosestPantoneColor(cmyk) {
    let closestColor = null;
    let minDistance = Infinity;
    
    pantoneColors.forEach(color => {
        const distance = Math.sqrt(
            Math.pow(cmyk.c - color.cmyk.c, 2) +
            Math.pow(cmyk.m - color.cmyk.m, 2) +
            Math.pow(cmyk.y - color.cmyk.y, 2) +
            Math.pow(cmyk.k - color.cmyk.k, 2)
        );
        
        if (distance < minDistance) {
            minDistance = distance;
            closestColor = color;
        }
    });
    
    return {
        color: closestColor,
        distance: minDistance,
        similarity: Math.max(0, 100 - (minDistance / 4))
    };
}

/**
 * Get Pantone Colors by Color Family (based on name patterns)
 * @param {string} family - Color family ('Yellow', 'Red', 'Blue', etc.)
 * @returns {Array} Array of colors in the specified family
 */
export function getPantoneColorsByFamily(family) {
    const familyPatterns = {
        'Process': /Pro./,
        'Yellow': /s(1[0-2]d|[1-9]d|100)s/,
        'Orange': /(Orange|Or.)/,
        'Red': /(Red|Warm Red|Rub. Red|Rhod. Red)/,
        'Pink': /(Pink|Rhod.)/,
        'Purple': /(Purple|Violet)/,
        'Blue': /(Blue|Cyan)/,
        'Green': /Green/,
        'Gray': /(Gray|Grey|Gy)/,
        'Black': /Black/
    };
    
    const pattern = familyPatterns[family];
    if (!pattern) return [];
    
    return pantoneColors.filter(color => pattern.test(color.name));
}

/**
 * Generate Pantone Color Preset Options for UI Dropdown
 * Compatible with existing calculator.js expectations
 * @returns {Array} Array of option objects for dropdown population
 */
export function generatePantonePresetOptions() {
    const options = [];
    const families = ['Process', 'Yellow', 'Orange', 'Red', 'Pink', 'Purple', 'Blue', 'Green', 'Gray', 'Black'];
    
    families.forEach(family => {
        const familyColors = getPantoneColorsByFamily(family);
        
        if (familyColors.length > 0) {
            // Add family header
            options.push({
                type: 'header',
                label: `${family} Colors`,
                value: null
            });
            
            // Add colors in this family
            familyColors.forEach(color => {
                options.push({
                    type: 'color',
                    label: color.name,
                    value: color.name,
                    color: color
                });
            });
        }
    });
    
    return options;
}

